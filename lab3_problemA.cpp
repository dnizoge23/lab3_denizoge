#ifndef LAB3_PROBLEMA_H
#define LAB3_PROBLEMA_H

/**
 * Given an array of integers, find the length of the longest
 * contiguous subarray that is strictly increasing.
 *
 * For example:
 *   arr = {1, 2, 2, 3, 4}
 *   Longest increasing subarray = {2, 3, 4}, length = 3
 *
 * @param arr integer array
 * @param size length of array
 * @return length of the longest strictly increasing contiguous subarray
 */
int longestIncreasingSubarray(int* arr, int size);

#endif
